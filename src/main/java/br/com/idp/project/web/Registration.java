package br.com.idp.project.web;

public class Registration {

	private String host;
	private int port;
	
	public Registration() {
		
	}
	public Registration(String host, int port) {
		super();
		this.host = host;
		this.port = port;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
}
